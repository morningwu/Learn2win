<h5>Understand the basic conventions of written English  </h5>
	<ul>
		<li>Sight read a wide range of common, phonically irregular words, e.g. have, said, was</li>
		<li>Use knowledge of basic letter-sound relationships to read aloud a variety of simple texts</li>


	</ul>
<h5>Construct meaning from texts </h5>
	>
	<ul>
		<li>Use known parts of words or word association to work out the meaning of unknown words, e.g. happy/unhappy, care/careless, bath/bathroom</li>
		<li>Work out the meaning of an unknown word or expression by using visual </li>
		clues, context and knowledge of the world 
		<li>Recognize recurrent patterns in language structure, such as word structure, word order, sentence structure </li>
		<li>Understand the information provided on the book cover (e.g. on the spine or 
		blurb), index and glossary </li>
		<li>Recognize the format and language features of a variety of text types, e.g. 
		journals, letters, menus, reports</li>
		<li>Read written language in meaningful chunks </li>
		<li>Understand the connection between ideas by identifying cohesive devices, e.g. also, at last, because, first, however, if, therefore</li>
		<li>Predict the likely development of a topic by recognizing key words, using personal experiences, and making use of context and knowledge of the world </li>
		<li>Re-read to establish and confirm meaning </li>
		<li>Self-correct by using strategies such as checking understanding against predictions, re-reading, using context, reading further to clarify, asking for 
		help </li>
		<li>Recognize the presentation of ideas through headings, paragraphing, 
		spacing, italics, bold print and punctuation </li>
		<li>Understand intention, attitudes and feelings conveyed in a text by recognizing features such as the choice and use of language </li>
		<li>Skim a text to obtain a general impression and the gist or main ideas</li>

	</ul>
	<h5>Locate information and ideas </h5>
	<ul>
	
		<li>Scan a text to locate specific information by using strategies such as looking at headings and repeated words  </li>
		<li>Identify details that support the gist or main ideas</li>

	</ul>


