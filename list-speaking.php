<h1>Speaking</h1><hr/>

<h5>Speaking Skills<a href="http://www.edb.gov.hk/attachment/en/curriculum-development/kla/eng-edu/primary%201_6.pdf  ">(Abstracted from here)</a>(pp. 53-54, 69-70)</h5>

<h5>Present information, ideas and feelings clearly and coherently </h5>
	<ul>
		<li>Use appropriate registers when speaking to familiar interlocutors such as teachers and peers, e.g. May I go to the toilet?</li>
		<li>Apply grammar rules such as subject-verb agreement correctly, e.g. Peter plays football every Sunday.</li>
		<li>Connect ideas by using cohesive devices, e.g. also, at last, before </li>
		<li>Use gestures and facial expressions to convey meaning and intention</li>
		<li>Use appropriate intonation and stress, and vary volume, tone of voice and speed to convey intended meanings and feelings.</li>

	</ul>
<h5>Participate effectively in an oral interaction </h5>
	<strong>Open an interaction by </strong>
	<ul>
		<li>greeting someone in an appropriate manner </li>
		<li>introducing oneself giving some details </li>
		<li>eliciting a response by asking questions or providing information on a topic, e.g. I’ve borrowed three very interesting books. Would you like to have a look?</li>
	</ul>
	<strong>Maintain an interaction by</strong>
	<ul>
		
	<li>controlling participation in an interaction or group activities, e.g. taking one’s turn at the right moment and recognizing others’ desire to speak, e.g. It’s my turn … It’s your turn now.</li>
	<li>asking and responding to others’ opinions, e.g. Do you like that book? What do you think of (name of a character in the book)?</li>
	<li>acknowledging, agreeing or disagreeing, asking questions, replying, adding or giving examples and explaining, using formulaic expressions 
	where appropriate </li>
	<li>self-correcting or rephrasing questions and answers if they are not understood </li>
	<li>predicting the likely development of a conversation and responding accordingly</li>

	</ul>
	<strong>Close an interaction by</strong>
	<ul>
		<li>using appropriate formulaic expressions, e.g. See you tomorrow.</li>
		<li>giving reasons, e.g. Sorry. I have to see my teacher now.</li>
	</ul>

