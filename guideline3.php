<h1>Guidelines in creating teaching materials</h1><hr/>
<ul>
<li>“Fun” is the core value of our teaching model.  All teaching materials (including teaching video segments and learning missions) should be presented in a fun way. </li>
<li>“Teaching material” to include the following: </li>
<ul>
	<li>A title card</li>
	<li>A teaching video, with or without instructor’s narration or explanations</li>
	<li>Teaching mission(s) — exercise(s) of the study</li>
	<li>Learning flash cards – a simple review of what has been learnt</li>
</ul>
<li>Teaching topics we are recruiting are listed in the “current subjects recruiting” section. They are designed according to “The Education Curriculum in Hong Kong”.</li>
<li>Please also refer to the “Expected learning outcome” section, where you can examine if your content is suitable for our use in Hong Kong. </li>
<li>English is our primary subject to teach at this stage, we plan to expand to Mathematics, Chinese and General Education for all forms and classes in the future. </li>
<li>Adding cultural elements in your teaching materials at anytime is appreciated. Contents may talk about the story behind English words, or some discussions on social and individual values and manners. </li>
<li>We target at students in Primary 4 to 6 (aged from 9 to 12). The tone and manner should be suitable for them. </li>
<li>Use British (United Kingdom) English as the standard language.  Specify those with different spelling and pronunciation if necessary. </li>
<li>When you prepare your video, create and shoot your own footages; if you have to use materials produced and owned by others, you must obtain written permission (e.g. license) from the copyright owners; or you can use Creative Common materials (creativecommons.org) and specify the source.</li>
<li>You should also collect the signed “Talent release form” from all the people appearig in your video.  You may use our standard Talent Release form.             [link and create]</li>
<li>To be readily approved, please follow this guideline tightly. You can create your own topic but please specified and obtain go-ahead signal before you do the production. </li>
<li>Please reference to the document <a href="www.edb.gov.hk/attachment/en/curriculum-development/kla/eng-edu/primary%201_6.pdf">“English Language Education Key Learning Areas:  English Language Curriculum Guide (Primary 1-6)”, prepared by The Curriculum Development Council, Recommended for use in schools by The Education and Manpower Bureau, HKSAR, 2004.</a></li>
<li>Once you’ve prepared a set of learning materials according to our requirement, please upload to our platform. </li>
</ul>

