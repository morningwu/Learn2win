<h5>How to prepare “Title Card”?</h5>

<ul>
	<li>A Title Card is the brief description of your video.</li>
	<li>It is to appear at the beginning of your video</li>
	<li>It should freeze for a minimum of 3 seconds (stills) or a maximum of 5 seconds (animation) before the video starts.</li>
	<li>Format: The Title Card is to display the topic of the video in big types; it should be clear and easy to read characters.  e.g. “Breakfast at home”, “Seeing a friend in hospital” etc. </li>  
	<li>Show only the teaching topic, no company name or other information should be shown at the beginning of the video. </li>
	<li><a href="" class="btn">View a sample of a Title Card.</a></li>

</ul>
<h5>How to prepare “Video”?</h5>
<ul>
	<li>The video format should be in MP4.</li>
	<li>Always refer to the “Expected learning outcome” stipulated in this guideline when you prepare your video. </li>
	<li>When you design your contents, try to put in elements listed in “Categories of Text tapes that students should be exposed to and produce” at the expected outcome section. </li>
	<li>Suggested topics of each type of contents: See  “Avaliable topic recruiting”  </li>
